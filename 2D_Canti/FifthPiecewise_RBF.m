function [u, u_x, u_y]=FifthPiecewise_RBF(x,c,h)

r=sqrt((x(1)-c(1))^2+(x(2)-c(2))^2)/h;

if r<2/3
    u=0.01*((3-r*3/2)^5-6*(2-r*3/2)^5+15*(1-r*3/2)^5);
    if r==0
        u_x=0;
        u_y=0;
    else
        tmp=-0.01*15/2*((3-r*3/2)^4-6*(2-r*3/2)^4+15*(1-r*3/2)^4)/h/h/r;
        u_x=tmp*(x(1)-c(1));
        u_y=tmp*(x(2)-c(2));
    end
elseif r>=2/3 && r<4/3
    u=0.01*((3-r*3/2)^5-6*(2-r*3/2)^5);
    tmp=-0.01*15/2*((3-r*3/2)^4-6*(2-r*3/2)^4)/h/h/r;
    u_x=tmp*(x(1)-c(1));
    u_y=tmp*(x(2)-c(2));
else
    u=0.01*(3-r*3/2)^5;
    tmp=-0.01*15/2*((3-r*3/2)^4)/h/h/r;
    u_x=tmp*(x(1)-c(1));
    u_y=tmp*(x(2)-c(2));
end